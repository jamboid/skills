def _md_sizes(byts, ndev):
    return " / ".join(humanize_bytes(byts[i]) for i in range(min(ndev, len(byts))))


def build_resources_markdown(data):
    res = data.get("resources") or {}
    cats = res.get("categories") or []
    if not cats:
        return []
    devices = res.get("devices") or ["Desktop"]
    ndev = len(devices)
    out = ["## Page resources", ""]
    for para in paragraphs(res.get("intro")):
        out.append(para)
        out.append("")
    head = "| Category | Requests | " + " | ".join(devices[:ndev]) + " |"
    out.append(head)
    out.append("|---|---:|" + "|".join(["---:"] * ndev) + "|")
    for c in cats:
        byts = c.get("bytes", [0])
        sizes = " | ".join(humanize_bytes(byts[i]) for i in range(min(ndev, len(byts))))
        out.append("| {0} | {1} | {2} |".format(
            c.get("type", ""), (c.get("requests") or [""])[0], sizes))
    tot = res.get("total") or {}
    if tot:
        tb = tot.get("bytes", [0])
        sizes = " | ".join(humanize_bytes(tb[i]) for i in range(min(ndev, len(tb))))
        out.append("| **Total** | {0} | {1} |".format((tot.get("requests") or [""])[0], sizes))
    out.append("")
    for c in cats:
        assets = c.get("assets") or []
        big, small = _res_split(assets, ndev)
        foldable = len(small) >= MIN_FOLD
        shown = big if foldable else assets
        if not shown and not foldable:
            continue
        out.append("**{0}**".format(c.get("type", "")))
        out.append("")
        for a in shown:
            ahost = _res_host(a.get("url"))
            au = a.get("url", "")
            # CommonMark: bare link destinations can't hold spaces or unbalanced
            # parens (tracking urls often do); angle-wrap those.
            if any(ch in au for ch in " ()"):
                au = "<" + au + ">"
            host_md = " ([{0}]({1}))".format(ahost, au) if ahost else ""
            out.append("- `{0}`{1} — {2}".format(
                a.get("name", ""), host_md, _md_sizes(a.get("bytes", [0]), ndev)))
        if foldable:
            fk = [sum((a.get("bytes") or [0])[i] for a in small) for i in range(ndev)]
            out.append("- _+{0} files under {1} KB — {2}_".format(
                len(small), SMALL_BYTES // 1024, _md_sizes(fk, ndev)))
        out.append("")
    return out


def build_markdown(data):
    meta = data["meta"]
    out = []
    out.append("# Website Performance Review: " + site_name(meta))
    out.append("")
    bits = []
    if meta.get("url"):
        bits.append("**URL:** " + meta["url"])
    bits.append("**Date:** " + str(meta.get("date", date.today().isoformat())))
    if meta.get("scope"):
        bits.append("**Scope:** " + meta["scope"])
    if meta.get("audience"):
        bits.append("**Audience:** " + meta["audience"])
    out.append(" · ".join(bits))
    out.append("")

    # Summary
    out.append("## Summary")
    out.append("")
    for para in paragraphs(data.get("summary")):
        out.append(para)
        out.append("")

    # Metrics
    out.append("## Metrics")
    out.append("")
    for para in paragraphs((data.get("metrics") or {}).get("intro")):
        out.append(para)
        out.append("")
    for g in build_groups(data):
        out.append("### " + (g["device"] or "Device"))
        out.append("")
        for ds in g["datasets"]:
            name = ("Overall average" if ds["kind"] == "average" else ds["type"])
            head = "#### " + name
            if ds.get("perfScore") is not None:
                head += " — performance score {0}".format(int(ds["perfScore"]))
            out.append(head)
            out.append("")
            meta_bits = []
            if ds.get("source"):
                meta_bits.append("_" + ds["source"] + "_")
            if ds.get("reportUrl"):
                meta_bits.append("[full report]({0})".format(ds["reportUrl"]))
            for l in ds.get("sourceLinks") or []:
                meta_bits.append("[{0}]({1})".format(l["type"], l["url"]))
            if meta_bits:
                out.append(" · ".join(meta_bits))
                out.append("")
            out.append("| Metric | Value | Rating |")
            out.append("|---|---|---|")
            for item in ds["items"]:
                key = item.get("key")
                disp = metric_display(key, item.get("value"), item.get("display"))
                rating = metric_rating(key, item.get("value"), item.get("rating"))
                out.append("| {0} | {1} | {2} |".format(
                    METRIC_LABEL.get(key, key), disp, RATING_LABEL.get(rating, rating)))
            out.append("")

    # Page resources
    res_md = build_resources_markdown(data)
    if res_md:
        out.extend(res_md)

    # Architecture
    if str(data.get("architecture") or "").strip():
        out.append("## Site architecture")
        out.append("")
        for para in paragraphs(data["architecture"]):
            out.append(para)
            out.append("")

    # Performance
    perf = data.get("performance") or {}
    if perf.get("tests") or perf.get("findings"):
        out.append("## Performance")
        out.append("")
        if perf.get("tests"):
            out.append("### Tests")
            out.append("")
            for t in perf["tests"]:
                out.append("- " + t.get("label", "") + ": " + t.get("url", ""))
            out.append("")
        if perf.get("findings"):
            out.append("### Findings")
            out.append("")
            for f in sorted_findings(perf["findings"]):
                title = "**{0}** — {1}".format(f.get("id", ""), f.get("title", ""))
                tags = []
                if f.get("savingsDisplay"):
                    tags.append(f["savingsDisplay"])
                if f.get("confirmed") is False:
                    tags.append("candidate")
                if tags:
                    title += " _(" + ", ".join(tags) + ")_"
                out.append(title)
                out.append("")
                for para in paragraphs(f.get("body")):
                    out.append(para)
                    out.append("")

    # Accessibility
    acc = data.get("accessibility") or {}
    if acc.get("tests") or acc.get("findings"):
        out.append("## Accessibility")
        out.append("")
        if acc.get("tests"):
            out.append("### Tests")
            out.append("")
            for t in acc["tests"]:
                out.append("- " + t.get("label", "") + ": " + t.get("url", ""))
            out.append("")
        if acc.get("findings"):
            out.append("### Findings")
            out.append("")
            for b in acc["findings"]:
                out.append("- " + str(b))
            out.append("")

    # Design & UX
    if data.get("ux"):
        out.append("## Design and user experience")
        out.append("")
        for b in data["ux"]:
            out.append("- " + str(b))
        out.append("")

    # Conclusions
    out.append("## Conclusions")
    out.append("")
    for para in paragraphs(data.get("conclusions")):
        out.append(para)
        out.append("")

    # Priorities
    if data.get("priorities"):
        out.append("### Priority actions")
        out.append("")
        for i, p in enumerate(data["priorities"], 1):
            out.append("{0}. {1}".format(i, p))
        out.append("")

    # Glossary
    gkeys = used_glossary(data)
    if gkeys:
        out.append("## Glossary")
        out.append("")
        for k in gkeys:
            name, desc = GLOSSARY[k]
            out.append("- **{0}** — {1}".format(name, desc))
        out.append("")

    # Appendix
    runs = list(perf.get("tests") or []) + list(acc.get("tests") or [])
    if runs:
        out.append("## Appendix — raw test runs")
        out.append("")
        for t in runs:
            out.append("- " + t.get("label", "") + ": " + t.get("url", ""))
        out.append("")

    return "\n".join(out).rstrip() + "\n"



def _res_cat_key(t):
    """Map a category type to a palette/icon key, defaulting unknowns to Other."""
    return t if t in RES_CAT_ICON else "Other"


def _res_bars(byts, scale, t, ndev, multi):
    key = _res_cat_key(t)
    rows = []
    for i in range(min(ndev, len(byts))):
        w = max(1.5, 100 * byts[i] / scale) if scale else 1.5
        cls = "d" if i == 0 else "m"
        klab = RES_KEY[i] if (multi and i < len(RES_KEY)) else ""
        rows.append('<div class="brow ' + cls + '"><span class="k">' + klab + '</span>'
                    '<div class="track"><div class="fill ' + cls + '" '
                    'style="width:{0:.1f}%;background:var(--cat-{1})"></div></div></div>'.format(w, key))
    return '<div class="bars">' + "".join(rows) + '</div>'


def _res_sizes(byts, ndev):
    lines = []
    for i in range(min(ndev, len(byts))):
        cls = "d" if i == 0 else "m"
        lines.append('<div class="sline ' + cls + '">' + html.escape(humanize_bytes(byts[i])) + '</div>')
    return '<div class="szs">' + "".join(lines) + '</div>'


def _res_split(assets, ndev):
    """Return (big, small): assets whose larger measured size is < SMALL_BYTES are
    'small'. Order within each bucket is preserved."""
    big, small = [], []
    for a in assets:
        byts = a.get("bytes") or [0]
        (small if max(byts[:ndev] or byts) < SMALL_BYTES else big).append(a)
    return big, small


def _res_host(url):
    """Return the lowercased host of an asset url for display, or "" if none."""
    if not url:
        return ""
    return (urlparse(url if "://" in url else "http://" + url).hostname or "").lower()


def _split_name(name):
    """Split a filename into (head, tail) for CSS middle-truncation: the tail is
    pinned (kept whole) so the extension survives; the head ellipsises. Short
    names get an empty tail (no truncation needed)."""
    n = str(name)
    return (n[:-12], n[-12:]) if len(n) > 16 else (n, "")


def build_resources_html(data):
    res = data.get("resources") or {}
    cats = res.get("categories") or []
    if not cats:
        return '      <p class="empty-note">No resource breakdown provided.</p>'
    devices = res.get("devices") or ["Desktop"]
    ndev = len(devices)
    multi = ndev > 1
    cat_scale = max([c.get("bytes", [0])[0] for c in cats] or [0]) or 1

    # header — device toggle (multi) or the single device's name in the bars column
    if multi:
        toggle = '<div class="scope">' + "".join(
            '<button data-scope="' + v + '"' + (' class="active"' if v == "both" else '')
            + '>' + html.escape(lbl) + '</button>' for v, lbl in RES_SCOPES) + '</div>'
    else:
        toggle = '<span class="dev1">' + html.escape(devices[0]) + '</span>'
    out = ['      <div class="res" data-scope="both">',
           '        <div class="res-head"><div>Category / file</div>'
           '<div class="r reqh">Req</div><div class="bcol">' + toggle
           + '</div><div class="r">Size</div></div>']

    for c in cats:
        t = str(c.get("type", "Other"))
        key = _res_cat_key(t)
        assets = c.get("assets") or []
        big, small = _res_split(assets, ndev)
        foldable = len(small) >= MIN_FOLD
        shown = big if foldable else assets
        item_scale = max([max(a.get("bytes", [0])) for a in assets] or [0]) or 1
        req = (c.get("requests") or [""])[0]

        cat_toggle = ''
        if foldable:
            cat_toggle = ('<div class="scope cat-scope">'
                          '<button data-mode="all">All</button>'
                          '<button data-mode="fewer" class="active">Fewer</button></div>')
        det_attr = ' data-files="fewer"' if foldable else ''
        out.append('        <details' + det_attr + '><summary class="cat-sum">'
                   '<div class="cat-name">' + RES_CHEV
                   + '<span class="cat-icon" style="background:var(--tint-' + key + ')">'
                   + RES_CAT_ICON[key] + '</span>' + html.escape(t) + cat_toggle + '</div>'
                   '<div class="req">' + html.escape(str(req)) + '</div>'
                   '<div class="bcol">' + _res_bars(c.get("bytes", [0]), cat_scale, t, ndev, multi)
                   + '</div>' + _res_sizes(c.get("bytes", [0]), ndev) + '</summary>')

        def asset_row(a, extra=""):
            ahost = _res_host(a.get("url"))
            host_html = ('<a class="host" href="' + attr(a.get("url", "")) + '" '
                         'target="_blank" rel="noopener"><span>' + html.escape(ahost)
                         + '</span>' + EXT_SVG + '</a>') if ahost else ""
            nm = str(a.get("name", ""))
            head, tail = _split_name(nm)
            name_html = ('<span class="nm" title="' + attr(nm) + '"><span class="nm-h">'
                         + html.escape(head) + '</span><span class="nm-t">'
                         + html.escape(tail) + '</span></span>')
            return ('          <div class="asset-row' + extra + '">'
                    '<div class="fname">' + name_html + host_html + '</div><div class="req"></div>'
                    '<div class="bcol">' + _res_bars(a.get("bytes", [0]), item_scale, t, ndev, multi)
                    + '</div>' + _res_sizes(a.get("bytes", [0]), ndev) + '</div>')

        for a in shown:
            out.append(asset_row(a))
        if foldable:
            for a in small:
                out.append(asset_row(a, " small"))
            fk = [sum((a.get("bytes") or [0])[i] for a in small) for i in range(ndev)]
            out.append('          <div class="sum-row" style="--tint:var(--tint-' + key + ')">'
                       '<div class="sum">+' + str(len(small)) + ' files under '
                       + str(SMALL_BYTES // 1024) + ' KB</div><div class="req"></div>'
                       '<div class="bcol"></div>' + _res_sizes(fk, ndev) + '</div>')
        out.append('        </details>')

    out.append('        <div class="res-foot"><div class="lbl">Total</div><div class="req">'
               + html.escape(str((res.get("total", {}).get("requests") or [""])[0]))
               + '</div><div class="bcol"></div>'
               + _res_sizes((res.get("total", {}).get("bytes") or [0]), ndev) + '</div>')
    out.append('      </div>')
    return "\n".join(out)


def build_findings_html(findings):
    rows = []
    for f in sorted_findings(findings):
        sev = str(f.get("severity", "low"))
        rows.append('      <article class="finding ' + sev + '">')
        rows.append('        <div class="finding-header">')
        if f.get("id"):
            rows.append('          <span class="finding-id">' + html.escape(str(f["id"])) + '</span>')
        rows.append('          <span class="finding-title">' + inline_html(f.get("title", "")) + '</span>')
        if f.get("savingsDisplay"):
            rows.append('          <span class="savings-chip">' + html.escape(f["savingsDisplay"]) + '</span>')
        if f.get("confirmed") is False:
            rows.append('          <span class="candidate-tag">Candidate</span>')
        rows.append('        </div>')
        rows.append('        <div class="finding-body">')
        for para in paragraphs(f.get("body")):
            rows.append('          <p>' + inline_html(para) + '</p>')
        if f.get("source"):
            rows.append('          <div class="finding-source">' + html.escape(f["source"]) + '</div>')
        rows.append('        </div>')
        rows.append('      </article>')
    return "\n".join(rows)


def build_tests_html(tests, label):
    rows = ['      <div class="subhead">' + html.escape(label) + '</div>',
            '      <ul class="test-list">']
    for t in tests:
        url = t.get("url", "")
        name = html.escape(t.get("label", ""))
        if url:
            rows.append('        <li><a href="' + attr(url) + '">' + name + '</a></li>')
        else:
            rows.append('        <li>' + name + '</li>')
    rows.append('      </ul>')
    return "\n".join(rows)


def build_bullets_html(items):
    rows = ['      <ul class="bullets">']
    for b in items:
        rows.append('        <li>' + inline_html(b) + '</li>')
    rows.append('      </ul>')
    return "\n".join(rows)


def build_html(data, template, slug):
    meta = data["meta"]
    nav = ['    <a href="#overview" class="nav-top-link active" data-target="overview">Overview</a>']
    body = []

    # Summary — part of the Overview nav entry (header + summary), no own link
    body.append(section("summary", "Summary",
                        prose_block(data.get("summary"), "summary-text")
                        or '    <p class="empty-note">No summary provided.</p>'))

    # Metrics
    nav.append(nav_link("metrics", "Metrics"))
    intro = prose_block((data.get("metrics") or {}).get("intro"), "section-intro")
    metrics_body = (intro + "\n" + build_metrics_html(data)) if intro else build_metrics_html(data)
    body.append(section("metrics", "Metrics", metrics_body))

    # Page resources
    if (data.get("resources") or {}).get("categories"):
        nav.append(nav_link("resources", "Page resources"))
        res_intro = prose_block((data.get("resources") or {}).get("intro"), "section-intro")
        res_body = (res_intro + "\n" + build_resources_html(data)) if res_intro \
            else build_resources_html(data)
        body.append(section("resources", "Page resources", res_body))

    # Architecture
    if str(data.get("architecture") or "").strip():
        nav.append(nav_link("architecture", "Site architecture"))
        body.append(section("architecture", "Site architecture", prose_block(data["architecture"])))

    # Performance
    perf = data.get("performance") or {}
    if perf.get("tests") or perf.get("findings"):
        nav.append(nav_link("performance", "Performance"))
        parts = []
        if perf.get("tests"):
            parts.append(build_tests_html(perf["tests"], "Tests"))
        if perf.get("findings"):
            parts.append('      <div class="subhead">Findings</div>')
            parts.append(build_findings_html(perf["findings"]))
        body.append(section("performance", "Performance", "\n".join(parts)))

    # Accessibility
    acc = data.get("accessibility") or {}
    if acc.get("tests") or acc.get("findings"):
        nav.append(nav_link("accessibility", "Accessibility"))
        parts = []
        if acc.get("tests"):
            parts.append(build_tests_html(acc["tests"], "Tests"))
        if acc.get("findings"):
            parts.append('      <div class="subhead">Findings</div>')
            parts.append(build_bullets_html(acc["findings"]))
        body.append(section("accessibility", "Accessibility", "\n".join(parts)))

    # Design & UX
    if data.get("ux"):
        nav.append(nav_link("ux", "Design & UX"))
        body.append(section("ux", "Design and user experience", build_bullets_html(data["ux"])))

    # Conclusions
    nav.append(nav_link("conclusions", "Conclusions"))
    body.append(section("conclusions", "Conclusions",
                        prose_block(data.get("conclusions"))
                        or '    <p class="empty-note">No conclusions provided.</p>'))

    # Priorities
    if data.get("priorities"):
        nav.append(nav_link("priorities", "Priority actions"))
        items = "\n".join('      <li>' + inline_html(p) + '</li>' for p in data["priorities"])
        body.append(section("priorities", "Priority actions",
                            '    <ol class="priority-list">\n' + items + '\n    </ol>'))

    # Glossary
    gkeys = used_glossary(data)
    if gkeys:
        nav.append(nav_link("glossary", "Glossary"))
        defs = []
        for k in gkeys:
            name, desc = GLOSSARY[k]
            defs.append('      <dt>' + html.escape(name) + '</dt>')
            defs.append('      <dd>' + html.escape(desc) + '</dd>')
        body.append(section("glossary", "Glossary", '    <dl class="glossary">\n'
                            + "\n".join(defs) + '\n    </dl>'))

    # Appendix
    runs = list(perf.get("tests") or []) + list(acc.get("tests") or [])
    if runs:
        nav.append(nav_link("appendix", "Appendix"))
        body.append(section("appendix", "Appendix — raw test runs",
                            build_tests_html(runs, "Test runs")))

    # Header meta — a URL button leads, then key/value chips in keeping with it.
    meta_rows = []
    if meta.get("url"):
        meta_rows.append('        <a class="url-btn" href="' + attr(meta["url"])
                         + '" target="_blank" rel="noopener">' + GLOBE_SVG
                         + '<span>' + html.escape(display_url(meta["url"])) + '</span>'
                         + EXT_SVG + '</a>')

    def meta_chip(label, value):
        return ('        <span class="meta-chip"><span class="mc-k">' + html.escape(label)
                + '</span><span class="mc-v">' + html.escape(str(value)) + '</span></span>')

    meta_rows.append(meta_chip("Date", meta.get("date", date.today().isoformat())))
    if meta.get("auditedBy"):
        meta_rows.append(meta_chip("By", meta["auditedBy"]))
    if meta.get("audience"):
        meta_rows.append(meta_chip("Audience", meta["audience"]))

    page_title = "Website Performance Review: " + site_name(meta)
    replacements = {
        "{{TITLE}}": html.escape(page_title),
        "{{SITENAME}}": html.escape(site_name(meta)),
        "{{HEADER_META}}": "\n".join(meta_rows),
        "{{SIDEBAR_NAV}}": "\n".join(nav),
        "{{CONTENT}}": "\n\n".join(body),
    }
    out = template
    for key, value in replacements.items():
        out = out.replace(key, value)
    return out


# ── Main ─────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(description="Build performance audit reports.")
    parser.add_argument("audit", help="Path to audit.json")
    parser.add_argument("--out-dir", default=".", help="Output directory (default: .)")
    parser.add_argument("--template", default=None, help="Path to report-template.html")
    args = parser.parse_args()

    script_dir = Path(__file__).resolve().parent
    template_path = Path(args.template) if args.template \
        else script_dir.parent / "assets" / "report-template.html"
    if not template_path.is_file():
        fail("HTML template not found: " + str(template_path))

    data = load_audit(args.audit)
    meta = data["meta"]
    slug = slugify(meta.get("slug") or meta.get("title") or "audit")

    out_dir = Path(args.out_dir)
    html_dir = out_dir / "html"
    out_dir.mkdir(parents=True, exist_ok=True)
    html_dir.mkdir(parents=True, exist_ok=True)

    md_path = out_dir / (slug + "-audit.md")
    html_path = html_dir / (slug + ".html")

    md_path.write_text(build_markdown(data), encoding="utf-8")
    template = template_path.read_text(encoding="utf-8")
    html_path.write_text(build_html(data, template, slug), encoding="utf-8")

    print("Performance audit reports written:")
    print("  Markdown : " + str(md_path))
    print("  HTML     : " + str(html_path))


if __name__ == "__main__":
    main()
